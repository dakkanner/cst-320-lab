#include "cIntExpr.h"

/************************************************************************
* cIntExpr(int val);
*		C'tor (default)
************************************************************************/
cIntExpr::cIntExpr(int val)
	:mVal(val)
{ }

/************************************************************************
* string toString();
*		Converts the data to a string.
************************************************************************/
string cIntExpr::toString()
{
	return "(EXPR: " + std::to_string(mVal) + ')';
}
